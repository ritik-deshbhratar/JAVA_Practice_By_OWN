package JAVABYTEJASSIR.NestedLoopBYTejassir;

public class QAN1 {
  public static void main(String[] args) {
    for (int i = 0; i < 3; i++) {
      for (int j = 0; j < 3; j++) {
        System.out.println("Good mornign");
      }
      System.out.println();
    }
  }
}
