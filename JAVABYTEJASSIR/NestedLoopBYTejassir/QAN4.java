package JAVABYTEJASSIR.NestedLoopBYTejassir;

public class QAN4 {
  public static void main(String[] args) {
    for (int j = 0; j < 4; j++) {
      for (int i = 0; i < 4 - j; i++) {
        System.out.print("* " + "  ");
      }
      System.out.println();
    }
  }
}
